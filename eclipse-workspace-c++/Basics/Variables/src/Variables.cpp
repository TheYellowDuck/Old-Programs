#include <iostream>
using namespace std;

int main() {
	string name = "Edward";
	int age = 6;
	cout << "There once was a man named " << name << "." << endl;
	cout << "He was " << age << " years old." << endl;

	name = "George";

	cout << "He liked the name " << name << "." << endl;
	cout << "He did not like being " << age << "." << endl;
	return 0;
}
