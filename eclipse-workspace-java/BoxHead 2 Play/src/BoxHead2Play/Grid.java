	public void display(Sketch sketch) {
		for (int i = 0; i < grid.length; i++) {
			for (int j = 0; j < grid[i].length; j++) {
				switch (grid[i][j]) {
				case 1:
					sketch.fill(200, 200, 200);
					sketch.square(j * TS + TS / 2, i * TS + TS / 4, TS);
					sketch.fill(250, 250, 250);
					sketch.square(j * TS, i * TS, TS);
					break;
				}
			}
		}
	}

